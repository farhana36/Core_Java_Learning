package condition;
//print even and odd no from 1 to 10 using if else condition
public class ifelsecond {
    public static void main(String[] args) {
        for(int i=1; i<=10; i++)
        {
            if(i%2==0)
            {
                System.out.println(i + " Nos are Even");
            }
            else
            {
                System.out.println(i + " Nos are odd");
            }

        }
    }
}
